package th.co.gosoft;

public class PostToday {
    private String title;
    private int amountOfItem;
    private String[] listOfItemTitle;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getAmountOfItem() {
        return amountOfItem;
    }

    public void setAmountOfItem(int amountOfItem) {
        this.amountOfItem = amountOfItem;
    }

    public String[] getListOfItemTitle() {
        return listOfItemTitle;
    }

    public void setListOfItemTitle(String[] listOfItemTitle) {
        this.listOfItemTitle = listOfItemTitle;
    }
}
